// --- I2C PERIPHERAL INITIALIZATION ---
void I2C1_Init(void) {
    // 1. Enable Clock for GPIOB and I2C1
    RCC->AHB1ENR |= RCC_AHB1ENR_GPIOBEN;
    RCC->APB1ENR |= RCC_APB1ENR_I2C1EN;

    // 2. Configure PB8 (SCL) and PB9 (SDA) for Alternate Function (AF4)
    GPIOB->MODER &= ~((3 << 16) | (3 << 18));
    GPIOB->MODER |= ((2 << 16) | (2 << 18));   // Alternate function mode
    GPIOB->OTYPER |= (1 << 8) | (1 << 9);      // Open-drain
    GPIOB->OSPEEDR |= ((3 << 16) | (3 << 18)); // High speed
    GPIOB->PUPDR |= ((1 << 16) | (1 << 18));   // Pull-ups enabled
    
    // Set AF4 (I2C1) for pins 8 and 9
    GPIOB->AFR[1] &= ~((0xF << 0) | (0xF << 4));
    GPIOB->AFR[1] |= ((4 << 0) | (4 << 4));

    // 3. I2C Configuration (Assuming 16MHz APB1 Clock)
    I2C1->CR1 |= I2C_CR1_SWRST;  // Software reset I2C1
    I2C1->CR1 &= ~I2C_CR1_SWRST; // Clear reset
    
    I2C1->CR2 = 16;     // Peripheral clock frequency = 16 MHz
    I2C1->CCR = 80;     // 100kHz standard mode speed
    I2C1->TRISE = 17;   // Maximum rise time
    
    I2C1->CR1 |= I2C_CR1_PE; // Enable I2C peripheral
}

// --- BASIC I2C TRANSMISSION PROTOCOL ---
void I2C_Write(uint8_t control_byte, uint8_t data) {
    // Wait until I2C is not busy
    while(I2C1->SR2 & I2C_SR2_BUSY);
    
    // Generate START condition
    I2C1->CR1 |= I2C_CR1_START;
    while(!(I2C1->SR1 & I2C_SR1_SB)); // Wait for Start Bit
    
    // Send Slave Address
    I2C1->DR = OLED_ADDR;
    while(!(I2C1->SR1 & I2C_SR1_ADDR)); // Wait for Address matched
    (void)I2C1->SR2; // Clear ADDR flag by reading SR2
    
    // Send Control Byte (0x00 for Command, 0x40 for Data)
    I2C1->DR = control_byte;
    while(!(I2C1->SR1 & I2C_SR1_TXE)); // Wait for TX Empty
    
    // Send Data Byte
    I2C1->DR = data;
    while(!(I2C1->SR1 & I2C_SR1_BTF)); // Wait for Byte Transfer Finished
    
    // Generate STOP condition
    I2C1->CR1 |= I2C_CR1_STOP;
}

void OLED_WriteCmd(uint8_t cmd) { I2C_Write(0x00, cmd); }
void OLED_WriteData(uint8_t data) { I2C_Write(0x40, data); }

// --- SSD1306 COMMANDS ---
void OLED_Init(void) {
		int i = 0;
    I2C1_Init();
    
    // Give the OLED a tiny bit of time to power stabilize before sending commands
    for(i = 0; i < 400000; i++) { __NOP(); } 

    OLED_WriteCmd(0xAE); // 1. Display OFF
    
    OLED_WriteCmd(0xD5); // 2. Set Display Clock Divide Ratio
    OLED_WriteCmd(0x80); //    Default value
    
    OLED_WriteCmd(0xA8); // 3. Set Multiplex Ratio (Rows)
    OLED_WriteCmd(0x1F); //    CRITICAL: 0x1F for 128x32 OLEDs (0x3F for 128x64)
    
    OLED_WriteCmd(0xD3); // 4. Set Display Offset
    OLED_WriteCmd(0x00); //    No offset
    
    OLED_WriteCmd(0x40); // 5. Set Start Line Address at 0
    
    OLED_WriteCmd(0x8D); // 6. Charge Pump Setting
    OLED_WriteCmd(0x14); //    Enable Charge Pump (Generates high voltage for pixels)
    
    OLED_WriteCmd(0x20); // 7. Set Memory Addressing Mode
    OLED_WriteCmd(0x00); //    Horizontal Addressing Mode
    
    OLED_WriteCmd(0xA1); // 8. Segment Re-map (Flips screen horizontally)
    OLED_WriteCmd(0xC8); // 9. COM Scan Direction (Flips screen vertically)
    
    OLED_WriteCmd(0xDA); // 10. Set COM Pins Hardware Configuration
    OLED_WriteCmd(0x02); //     CRITICAL: 0x02 for 128x32 (0x12 is for 128x64)
    
    OLED_WriteCmd(0x81); // 11. Set Contrast Control
    OLED_WriteCmd(0x8F); //     Mid-high contrast
    
    OLED_WriteCmd(0xD9); // 12. Set Pre-charge Period
    OLED_WriteCmd(0xF1); 
    
    OLED_WriteCmd(0xDB); // 13. Set VCOMH Deselect Level
    OLED_WriteCmd(0x40); 
    
    OLED_WriteCmd(0xA4); // 14. Entire Display ON (Resume to RAM content)
    OLED_WriteCmd(0xA6); // 15. Set Normal Display (0xA7 would invert colors)
    
    OLED_WriteCmd(0xAF); // 16. Display ON
    
    OLED_Clear();        // Wipe any random startup static in the RAM
}

void OLED_SetCursor(uint8_t row, uint8_t col) {
    OLED_WriteCmd(0xB0 + row);             // Set Page Start Address (0-7 for 0.96", 0-3 for 0.91")
    OLED_WriteCmd(0x00 + (col & 0x0F));    // Set Lower Column Start Address
    OLED_WriteCmd(0x10 + ((col >> 4) & 0x0F)); // Set Higher Column Start Address
}

void OLED_Clear(void) {
		uint8_t i = 0;
		uint8_t j = 0;
    for (i = 0; i < 4; i++) { // 4 pages for 0.91" (128x32)
        OLED_SetCursor(i, 0);
        for (j = 0; j < 128; j++) {
            OLED_WriteData(0x00);
        }
    }
}

void OLED_PrintChar(char c) {
		uint8_t i = 0;
    if (c < 32 || c > 122) c = 32; // Default to space if out of bounds
		for (i = 0; i < 5; i++) {
        OLED_WriteData(Font5x8[c - 32][i]);
    }
    OLED_WriteData(0x00); // 1 pixel spacing between letters
}

void OLED_PrintString(char* str) {
    while (*str) {
        OLED_PrintChar(*str++);
    }
}
